    class Generator //publisher
    {
        List<string> list = new List<string>();
        public event EventHandler pwAdded;

        public string this[int i]
        {
            get
            {
                return list[i];
            }
            set
            {
                list.Add(value);
                if (pwAdded != null)
                {
                    pwAdded?.Invoke(this, null);
                }
            }
        }
    }
    class Subscriber //subscriber
    {
        private Generator generator;

        public void SubscribeToEvent(Generator publisher)
        {
            generator = publisher;
            generator.pwAdded += Publisher_pwAdded;
        }

        private void Publisher_pwAdded(object sender, EventArgs eventArgs)
        {
            Console.WriteLine(sender + " is added to list.");
        }

        public void UnsubscribeToEvent()
        {
            generator.pwAdded -= Publisher_pwAdded;
        }
    }
    class DelegateGeneratorKey //main
    {
        static void Main(string[] args)
        {
            Console.WriteLine("--Delegate Generator Key--");
            Console.WriteLine("Please enter a password to use:");
            string password = Console.ReadLine();
            Console.WriteLine("Please enter a string to encrypt:");
            string plaintext = Console.ReadLine();

            Generator generator = new Generator();
            Subscriber subscriber = new Subscriber();
            subscriber.SubscribeToEvent(generator);
            bool loop = true;
            int count = 0;

